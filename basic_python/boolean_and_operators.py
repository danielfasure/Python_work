#comparasion operator
#when using them will return a booleon True or False
print(1==2) # == mean is it the same value
print(1!=2)#!= means are they not the same value
print(1>2) # > the value on the left side is compared and if not greater than value on the right then false
print(1<2) # < the value on the left is smaller than right value print True
print(1>=2)# value on the left can be greater and be equal to value on the right
print(1<=2)# value on the left can be smaller and equal to value on right to print True

#logicaL operator
print(1>3 and 5<7)# and means that both statement has to be true
print(1>3 or 2<7) # or means that only one statement has to be true to print true
print(not(1==1))#if the statement print true then it will turn it false and vice versa
